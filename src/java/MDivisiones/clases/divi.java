
package MDivisiones.clases;

/**
 *
 * @author alis0
 */
public class divi {
    int id;
    String nombre;
    int idj;
    int ide;
    
    public divi() {
        
    }

    public divi(int id, String nombre, int idj, int ide) {
        this.id = id;
        this.nombre = nombre;
        this.idj = idj;
        this.ide = ide;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getIdj() {
        return idj;
    }

    public void setIdj(int idj) {
        this.idj = idj;
    }

    public int getIde() {
        return ide;
    }

    public void setIde(int ide) {
        this.ide = ide;
    }
    
    
}
